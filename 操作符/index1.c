#include <stdio.h>

int main()
{
    //a和b是char型，没有达到一个int的大小，会发生整型提升
    //3的二进制：00000000 00000000 00000000 00000011
    //最后放入a的是：00000011
    char a = 3;
    //127的二进制是：00000000 00000000 00000000 01111111
    //最后放入b的是：01111111
    char b = 127;
    char c = a + b;
    printf("%d\n",c);
    return 0;
}